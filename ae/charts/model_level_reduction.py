def _per_model_reduction(df_trace):
    """Compute per-model reduction % from a cumulative trace DataFrame."""
    cum_orig = df_trace["cum_original_bytes"].values.astype(float)
    cum_stor = df_trace["cum_stored_bytes"].values.astype(float)
    per_orig = np.diff(cum_orig, prepend=0)
    per_stor = np.diff(cum_stor, prepend=0)
    result = df_trace[["family"]].copy()
    result["reduction"] = np.where(per_orig > 0, (1.0 - per_stor / per_orig) * 100.0, np.nan)
    result = result.dropna(subset=["reduction"])
    return result[result["family"].str.lower() != "other"]


def chart_reduction_violin_by_family(rc):
    """Side-by-side violin plot: TensorDex-TX vs TensorDex-FM vs ZipLLM per-model reduction."""
    df_fs, df_zp = _load_trace_csvs()
    if df_fs is None:
        return _missing_data_fig("Data not found.\nRun plots/model_level_reduction/plot.py first.")

    # TensorDex-TX per-model reduction
    red_tx = _per_model_reduction(df_fs)

    # ZipLLM (was ZipLLM-Oracle) per-model reduction
    red_zp = _per_model_reduction(df_zp) if df_zp is not None and not df_zp.empty else None

    # TensorDex-FM per-model reduction (from FM-Delta trace)
    _fm_traces = _build_fm_traces(df_fs)
    tdx_fm_trace = _fm_traces.get("flexsplit")
    red_fm = None
    if tdx_fm_trace is not None:
        cum_orig = df_fs["cum_original_bytes"].values.astype(float)
        per_orig = np.diff(cum_orig, prepend=0)
        # Derive per-model stored from cumulative reduction trace
        cum_fm_stored = cum_orig * (1.0 - tdx_fm_trace / 100.0)
        per_fm_stored = np.diff(cum_fm_stored, prepend=0)
        fm_red = np.where(per_orig > 0, (1.0 - per_fm_stored / per_orig) * 100.0, np.nan)
        red_fm = df_fs[["family"]].copy()
        red_fm["reduction"] = fm_red
        red_fm = red_fm.dropna(subset=["reduction"])
        red_fm = red_fm[red_fm["family"].str.lower() != "other"]

    # Fixed family order: similar models grouped together
    _FAMILY_ORDER = [
        "Qwen/Qwen2.5-7B", "Qwen/Qwen3-8B",
        "mistralai/Mistral-7B-v0.1", "mistralai/Mistral-7B-Instruct-v0.3",
        "google/gemma-2-9b-it", "google/gemma-3-4b-it",
        "meta-llama/Meta-Llama-3-8B", "meta-llama/Llama-3.1-8B",
        "meta-llama/Llama-3.1-8B-Instruct", "meta-llama/Llama-3.2-3B",
    ]
    available = set(red_tx["family"].unique())
    family_order = [f for f in _FAMILY_ORDER if f in available]
    # Append any remaining families not in the fixed order
    for f in sorted(available):
        if f not in family_order and f.lower() != "other":
            family_order.append(f)

    # Short labels for x-axis
    _FAMILY_LABELS = {
        "Qwen/Qwen2.5-7B":                       "Qwen2.5",
        "Qwen/Qwen3-8B":                          "Qwen3",
        "mistralai/Mistral-7B-v0.1":              "Mistral-v0.1",
        "mistralai/Mistral-7B-Instruct-v0.3":     "Mistral-v0.3",
        "meta-llama/Meta-Llama-3-8B":             "Llama-3",
        "meta-llama/Llama-3.1-8B":                "Llama-3.1",
        "meta-llama/Llama-3.1-8B-Instruct":       "Llama-3.1-Inst",
        "meta-llama/Llama-3.2-3B":                "Llama-3.2",
        "google/gemma-2-9b-it":                   "Gemma-2",
        "google/gemma-3-4b-it":                   "Gemma-3",
    }
    def _shorten(f):
        return _FAMILY_LABELS.get(f, f.split("/")[-1] if "/" in f else f)
    short_labels = [_shorten(f) for f in family_order]

    data_tx = [red_tx.loc[red_tx["family"] == f, "reduction"].values for f in family_order]
    data_fm = [red_fm.loc[red_fm["family"] == f, "reduction"].values for f in family_order] if red_fm is not None else None
    data_zp = [red_zp.loc[red_zp["family"] == f, "reduction"].values for f in family_order] if red_zp is not None else None

    fig, ax = plt.subplots(figsize=(rc["figsize_w"], rc["figsize_h"]))

    n = len(family_order)
    positions = np.arange(n)
    width = 0.28

    color_tx = "#b3de69"                  # TensorDex-TX (swapped)
    color_fm = COLORS_LOCAL["flexsplit"]   # TensorDex-FM (swapped)
    color_zp = COLORS_LOCAL["zipllm"]      # ZipLLM (was ZipLLM-Oracle)

    def _draw_violin(ax, data, pos, color, med_color):
        parts = ax.violinplot(data, positions=pos,
                              widths=width * 0.9, showmedians=False, showextrema=False)
        for body in parts["bodies"]:
            body.set_facecolor(color)
            body.set_edgecolor("black")
            body.set_linewidth(0.6)
            body.set_alpha(0.7)
        bp = ax.boxplot(data, positions=pos, widths=width * 0.2,
                        patch_artist=True, showfliers=False, zorder=3)
        for patch in bp["boxes"]:
            patch.set_facecolor("white")
            patch.set_edgecolor("black")
            patch.set_linewidth(0.8)
        for el in ["whiskers", "caps"]:
            for line in bp[el]:
                line.set_color("black")
                line.set_linewidth(0.8)
        for line in bp["medians"]:
            line.set_color(med_color)
            line.set_linewidth(1.8)

    # ── TensorDex-FM violins (left) ──
    if data_fm is not None:
        _draw_violin(ax, data_fm, positions - width, color_fm, color_fm)
    # ── TensorDex-TX violins (center) ──
    _draw_violin(ax, data_tx, positions, color_tx, color_tx)
    # ── ZipLLM violins (right) ──
    if data_zp is not None:
        _draw_violin(ax, data_zp, positions + width, color_zp, color_zp)

    # X-axis: short family names
    ax.set_xticks(positions)
    ax.set_xticklabels(short_labels, fontsize=rc["tick_label_size"] * 0.7, rotation=15, ha="center")
    ax.tick_params(axis="y", labelsize=rc["tick_label_size"])
    ax.set_ylabel("Per-Model Reduction %", fontsize=rc.get("label_size", 42))
    ax.set_xlabel("Model Family", fontsize=rc.get("label_size", 42))
    ax.set_ylim(bottom=-5)
    ax.yaxis.set_major_locator(mticker.MaxNLocator(nbins=5))
    ax.yaxis.set_major_formatter(mticker.FormatStrFormatter("%.0f%%"))

    # Method legend
    from matplotlib.patches import Patch
    legend_items = [
        Patch(facecolor=color_fm, edgecolor="black", label="TensorDex-FM"),
        Patch(facecolor=color_tx, edgecolor="black", label="TensorDex-TX"),
        Patch(facecolor=color_zp, edgecolor="black", label="ZipLLM"),
    ]
    ax.legend(handles=legend_items, loc="lower right",
              fontsize=rc["legend_size"], frameon=True,
              handlelength=1.0, handletextpad=0.4)

    plt.tight_layout()
    return fig


