def _sim_load_data():
    global _data_cache
    if _data_cache is not None:
        return _data_cache
    conn = sqlite3.connect(_METADATA_DB)
    tensor_sizes = dict(conn.execute("SELECT id, size_bytes FROM tensors").fetchall())
    model_tensors = defaultdict(list)
    for m, t in conn.execute("SELECT model_name, tensor_id FROM model_mappings").fetchall():
        model_tensors[m].append(t)
    conn.close()
    model_tensors = {m: list(set(ts)) for m, ts in model_tensors.items()}
    with open(_CACHE_JSON) as f:
        created_at = json.load(f)
    _data_cache = (tensor_sizes, model_tensors, created_at)
    _pcache['_data_cache'] = _data_cache
    return _data_cache


_plan_cache = _pcache.get('_plan_cache', {})

def _sim_load_plan(csv_path, col):
    cache_key = (csv_path, col)
    if cache_key in _plan_cache:
        return _plan_cache[cache_key]
    df = pd.read_csv(csv_path, low_memory=False)
    df = df[df["target_id"] != df["base_id"]].copy()
    if "bytes_in" in df.columns:
        df = df[df["bytes_in"] > 0]
    df = df.dropna(subset=[col]).copy()
    df[col] = df[col].astype(int)
    df = df.sort_values(col).drop_duplicates("target_id", keep="first")
    tids = df["target_id"].astype(str).values
    bids = df["base_id"].astype(str).values
    vals = df[col].values.astype(int)
    result = {t: (b, int(v)) for t, b, v in zip(tids, bids, vals)}
    _plan_cache[cache_key] = result
    _pcache['_plan_cache'] = _plan_cache
    return result


_keep_models_cache = None

def _get_keep_models():
    """Return the set of model names kept by ZipLLM-Real sampling (cached)."""
    global _keep_models_cache
    if _keep_models_cache is not None:
        return _keep_models_cache
    tensor_sizes, model_tensors, created_at = _sim_load_data()
    zipllm_plan = _sim_load_plan(_ZIPLLM_CSV, "bytes_out")
    model_planned = defaultdict(set)
    for m, tids in model_tensors.items():
        for tid in tids:
            if tid in zipllm_plan:
                model_planned[m].add(tid)
    models_with_plan = sorted(m for m in model_planned if model_planned[m])
    rng = np.random.default_rng(_SEED)
    n_keep = max(1, int(len(models_with_plan) * _SAMPLE_FRAC))
    _keep_models_cache = set(rng.choice(models_with_plan, size=n_keep, replace=False))
    return _keep_models_cache


def _sim_build_zipllm_real(zipllm_plan, tensor_sizes, model_tensors, created_at):
    model_planned = defaultdict(set)
    tensor_to_models = defaultdict(set)
    for m, tids in model_tensors.items():
        for tid in tids:
            if tid in zipllm_plan:
                model_planned[m].add(tid)
                tensor_to_models[tid].add(m)

    models_with_plan = sorted(m for m in model_planned if model_planned[m])
    rng = np.random.default_rng(_SEED)
    n_keep = max(1, int(len(models_with_plan) * _SAMPLE_FRAC))
    keep_models = set(rng.choice(models_with_plan, size=n_keep, replace=False))

    kept_tensors = set()
    degraded_tensors = set()
    for tid in zipllm_plan:
        owners = tensor_to_models.get(tid, set())
        if owners & keep_models:
            kept_tensors.add(tid)
        else:
            degraded_tensors.add(tid)

    modified_plan = {}
    savings = []
    for tid, (bid, bout) in zipllm_plan.items():
        size = tensor_sizes.get(tid, 0)
        if size <= 0:
            continue
        if tid in kept_tensors:
            modified_plan[tid] = (bid, bout)
            if size >= _MIN_TENSOR_BYTES:
                savings.append((1.0 - bout / size) * 100.0)
        else:
            new_bout = int(size * (1.0 - _FIXED_REDUCTION))
            modified_plan[tid] = (bid, new_bout)
            if size >= _MIN_TENSOR_BYTES:
                savings.append(_FIXED_REDUCTION * 100.0)

    return np.array(savings), modified_plan


def _sim_per_tensor_savings(plan, tensor_sizes):
    savings = []
    for tid, (bid, bout) in plan.items():
        size = tensor_sizes.get(tid, 0)
        if size >= _MIN_TENSOR_BYTES:
            savings.append((1.0 - bout / size) * 100.0)
    return np.array(savings)


def _sim_overall_reduction(plan, tensor_sizes, model_tensors, created_at):
    children = defaultdict(list)
    for tid, (bid, _) in plan.items():
        children[bid].append(tid)
    gains = {tid: max(0, tensor_sizes.get(tid, 0) - bout)
             for tid, (bid, bout) in plan.items()}
    base_val = {bid: sum(gains.get(k, 0) for k in kids)
                for bid, kids in children.items()}
    conflicts = set(plan.keys()) & set(children.keys())
    forced = {n for n in conflicts if base_val.get(n, 0) > gains.get(n, 0)}

    final = {}
    roles = {}
    for tid, (bid, bout) in plan.items():
        if tid in forced:
            roles[tid] = "base"; continue
        if bid in conflicts and bid not in forced:
            roles[tid] = "base"; continue
        final[tid] = (bid, bout)
        roles[tid] = "target"
        if bid not in roles:
            roles[bid] = "base"

    all_timed = [(m, ts) for m, ts in created_at.items() if m in model_tensors]
    all_timed.sort(key=lambda x: x[1])
    processed = set()
    cum_orig = cum_stored = 0
    for model, ts in all_timed:
        for tid in model_tensors.get(model, []):
            if tid in processed:
                continue
            size = tensor_sizes.get(tid, 0)
            cum_orig += size
            role = roles.get(tid, "base")
            if role == "target" and tid in final:
                bid, bout = final[tid]
                cum_stored += bout
                processed.add(tid)
                if bid not in processed:
                    bs = tensor_sizes.get(bid, 0)
                    cum_stored += bs
                    cum_orig += bs
                    processed.add(bid)
            else:
                cum_stored += size
                processed.add(tid)

    reduction = (1.0 - cum_stored / cum_orig) * 100.0 if cum_orig > 0 else 0.0
    return cum_orig, cum_stored, reduction


_savings_cache = _pcache.get('_savings_cache')

def _sim_load_savings():
    """Load plans and compute only per-tensor savings (fast, no overall reduction)."""
    global _savings_cache
    if _savings_cache is not None:
        return _savings_cache
    tensor_sizes, model_tensors, created_at = _sim_load_data()

    zipllm_plan = _sim_load_plan(_ZIPLLM_CSV, "bytes_out")

    zipllm_real_savings, _ = _sim_build_zipllm_real(
        zipllm_plan, tensor_sizes, model_tensors, created_at)

    n_zipnn = sum(1 for tid in zipllm_plan
                  if tensor_sizes.get(tid, 0) >= _MIN_TENSOR_BYTES)

    # TensorDex-TX: use tbytes_out directly from the new FlexSplit CSV
    flexsplit_td = _sim_load_plan(_FLEXSPLIT_ZELTAX_CSV, "tbytes_out")

    # OpenZL: standalone compression
    _openzl_csv = str(_PROJECT_ROOT / "tests/output/openzl_benchmark.csv")
    oz_savings = np.array([])
    if os.path.exists(_openzl_csv):
        oz_plan = {}
        with open(_openzl_csv, newline="") as _f:
            for _r in csv.DictReader(_f):
                tid = _r["target_id"]
                oz = int(_r.get("openzl_standalone", 0) or 0)
                if oz > 0:
                    size = tensor_sizes.get(tid, 0)
                    if size >= _MIN_TENSOR_BYTES:
                        oz_savings = np.append(oz_savings, (1.0 - oz / size) * 100.0)

    # TensorDex-FM: FM-Delta on FlexSplit plan pairs
    fs_fm_savings = np.array([])
    _results_db = str(_PROJECT_ROOT / "results.db")
    if os.path.exists(_results_db):
        import sqlite3 as _sql
        _rconn = _sql.connect(_results_db)
        zipllm_fb = {}
        for tid, bid, fb, bi in _rconn.execute(
            "SELECT target_id, base_id, fbytes_out, bytes_in "
            "FROM compression_results WHERE fbytes_out > 0 AND bytes_in > 0"
        ).fetchall():
            zipllm_fb[(tid, bid)] = (int(fb), int(bi))

        flexsplit_plan_csv = str(
            _PROJECT_ROOT / "tests/output/flexsplit_all_2026-03-03_08-28-11"
            / "compression_data/real_compression_all_models.csv.before_tratio_bak"
        )
        if os.path.exists(flexsplit_plan_csv):
            import csv as _csv
            fs_fm_plan = {}
            with open(flexsplit_plan_csv, newline="") as _f:
                for _r in _csv.DictReader(_f):
                    tid, bid = _r["target_id"], _r["base_id"]
                    key = (tid, bid)
                    if key in zipllm_fb:
                        fs_fm_plan[tid] = (bid, zipllm_fb[key][0])
            if fs_fm_plan:
                fs_fm_savings = _sim_per_tensor_savings(fs_fm_plan, tensor_sizes)
        _rconn.close()

    # FM-Delta per-tensor savings: 12.4% sampled use fbytes_out, rest ZipNN 33.3%
    fm_delta_savings = []
    _results_db_path = str(_PROJECT_ROOT / "results.db")
    if os.path.exists(_results_db_path):
        import sqlite3 as _sql2
        _rconn2 = _sql2.connect(_results_db_path)
        _fb_map = {}
        for tid, bid, fb, bi in _rconn2.execute(
            "SELECT target_id, base_id, fbytes_out, bytes_in "
            "FROM compression_results WHERE fbytes_out > 0 AND bytes_in > 0"
        ).fetchall():
            _fb_map[(tid, bid)] = int(fb)
        _rconn2.close()
        # Build tid→models reverse index
        _tid_to_models = defaultdict(set)
        for m, tids in model_tensors.items():
            for t in tids:
                _tid_to_models[t].add(m)
        keep_models = _get_keep_models()
        for tid, (bid, _bout) in zipllm_plan.items():
            size = tensor_sizes.get(tid, 0)
            if size < _MIN_TENSOR_BYTES:
                continue
            if _tid_to_models.get(tid, set()) & keep_models:
                # Sampled: use fbytes_out if available, else original bytes_out
                key = (tid, bid)
                if key in _fb_map:
                    fm_delta_savings.append((1.0 - _fb_map[key] / size) * 100.0)
                else:
                    fm_delta_savings.append((1.0 - _bout / size) * 100.0)
            else:
                # Non-sampled: ZipNN 33.3%
                fm_delta_savings.append(33.3)
    fm_delta_savings = np.array(fm_delta_savings) if fm_delta_savings else zipllm_real_savings

    _savings_cache = OrderedDict([
        ("OpenZL",                                  oz_savings),
        ("ZipNN",                                   np.full(n_zipnn, 33.4)),
        ("ZipLLM",                                  fm_delta_savings),
        ("ZipLLM-Oracle",                           _sim_per_tensor_savings(zipllm_plan, tensor_sizes)),
        ("TensorDex-TX",                            _sim_per_tensor_savings(flexsplit_td, tensor_sizes)),
        ("TensorDex-FM",                            fs_fm_savings),
    ])
    _pcache['_savings_cache'] = _savings_cache
    return _savings_cache


_reductions_cache = _pcache.get('_reductions_cache')

def _sim_load_reductions():
    """Load plans and compute overall storage reductions (slow: chronological trace)."""
    global _reductions_cache
    if _reductions_cache is not None:
        return _reductions_cache
    tensor_sizes, model_tensors, created_at = _sim_load_data()

    zipllm_plan = _sim_load_plan(_ZIPLLM_CSV, "bytes_out")

    _, zipllm_real_plan = _sim_build_zipllm_real(
        zipllm_plan, tensor_sizes, model_tensors, created_at)

    zipnn_orig, _, _ = _sim_overall_reduction(
        zipllm_real_plan, tensor_sizes, model_tensors, created_at)
    zipnn_stored = zipnn_orig * (1.0 - 0.334)

    reductions = OrderedDict()

    # OpenZL: standalone compression (from benchmark CSV)
    _openzl_csv = str(_PROJECT_ROOT / "tests/output/openzl_benchmark.csv")
    if os.path.exists(_openzl_csv):
        oz_plan = {}
        with open(_openzl_csv, newline="") as _f:
            for _r in csv.DictReader(_f):
                tid, bid = _r["target_id"], _r["base_id"]
                oz = int(_r.get("openzl_standalone", 0) or 0)
                if oz > 0:
                    oz_plan[tid] = (bid, oz)
        if oz_plan:
            orig, stored, red = _sim_overall_reduction(
                oz_plan, tensor_sizes, model_tensors, created_at)
            reductions["OpenZL"] = {
                "orig_gb": orig / 1e9,
                "stored_gb": stored / 1e9,
                "reduction": red,
            }

    reductions["ZipNN"] = {
        "orig_gb": zipnn_orig / 1e9,
        "stored_gb": zipnn_stored / 1e9,
        "reduction": 33.4,
    }
    # ZipLLM-Oracle
    orig, stored, red = _sim_overall_reduction(
        zipllm_plan, tensor_sizes, model_tensors, created_at)
    reductions["ZipLLM-Oracle"] = {
        "orig_gb": orig / 1e9,
        "stored_gb": stored / 1e9,
        "reduction": red,
    }

    # FM-Delta results from results.db (fbytes_out)
    _results_db = str(_PROJECT_ROOT / "results.db")
    if os.path.exists(_results_db):
        import sqlite3 as _sql
        _rconn = _sql.connect(_results_db)

        # Load FM-Delta compressed sizes
        zipllm_fb = {}
        for tid, bid, fb, bi in _rconn.execute(
            "SELECT target_id, base_id, fbytes_out, bytes_in "
            "FROM compression_results WHERE fbytes_out > 0 AND bytes_in > 0"
        ).fetchall():
            zipllm_fb[(tid, bid)] = (int(fb), int(bi))

        # ZipLLM (FM-Delta): 12.4% sampled models use fbytes_out,
        # remaining 87.6% fallback to ZipNN 33.3% reduction.
        # Build tid→models reverse index once (fast lookup)
        tid_to_models = defaultdict(set)
        for m, tids in model_tensors.items():
            for tid in tids:
                tid_to_models[tid].add(m)
        keep_models = _get_keep_models()
        fm_delta_plan = {}
        for tid, (bid, _bout) in zipllm_plan.items():
            size = tensor_sizes.get(tid, 0)
            if size <= 0:
                continue
            if tid_to_models.get(tid, set()) & keep_models:
                # Sampled: use fbytes_out if available, else original bytes_out
                key = (tid, bid)
                if key in zipllm_fb:
                    fm_delta_plan[tid] = (bid, zipllm_fb[key][0])
                else:
                    fm_delta_plan[tid] = (bid, _bout)
            else:
                # Non-sampled: ZipNN 33.3% reduction
                fm_delta_plan[tid] = (bid, int(size * 0.667))
        if fm_delta_plan:
            orig, stored, red = _sim_overall_reduction(
                fm_delta_plan, tensor_sizes, model_tensors, created_at)
            reductions["ZipLLM"] = {
                "orig_gb": orig / 1e9,
                "stored_gb": stored / 1e9,
                "reduction": red,
            }

        # TensorDex-FM: FM-Delta on FlexSplit plan pairs
        flexsplit_plan_csv = str(
            _PROJECT_ROOT / "tests/output/flexsplit_all_2026-03-03_08-28-11"
            / "compression_data/real_compression_all_models.csv.before_tratio_bak"
        )
        if os.path.exists(flexsplit_plan_csv):
            import csv as _csv
            fs_fm_plan = {}
            with open(flexsplit_plan_csv, newline="") as _f:
                for _r in _csv.DictReader(_f):
                    tid, bid = _r["target_id"], _r["base_id"]
                    key = (tid, bid)
                    if key in zipllm_fb:
                        fs_fm_plan[tid] = (bid, zipllm_fb[key][0])
            if fs_fm_plan:
                orig, stored, red = _sim_overall_reduction(
                    fs_fm_plan, tensor_sizes, model_tensors, created_at)
                reductions["TensorDex-FM"] = {
                    "orig_gb": orig / 1e9,
                    "stored_gb": stored / 1e9,
                    "reduction": red,
                }

        _rconn.close()

    # TensorDex: read from trace CSV (already uses tratio)
    df_fs, _ = _load_trace_csvs()
    if df_fs is not None and not df_fs.empty:
        last = df_fs.iloc[-1]
        td_orig = float(last["cum_original_bytes"])
        td_stored = float(last["cum_stored_bytes"])
        td_red = float(last["reduction_pct"])
        reductions["TensorDex-TX"] = {
            "orig_gb": td_orig / 1e9,
            "stored_gb": td_stored / 1e9,
            "reduction": td_red,
        }

    _reductions_cache = reductions
    _pcache['_reductions_cache'] = _reductions_cache
    return _reductions_cache


