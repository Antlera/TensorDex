def _build_fm_traces(df_fs):
    """Build per-model-step cumulative reduction traces for FM-Delta plans."""
    global _fm_trace_cache
    if _fm_trace_cache is not None:
        return _fm_trace_cache

    _results_db = str(_PROJECT_ROOT / "results.db")
    if not os.path.exists(_results_db):
        _fm_trace_cache = {}
        return _fm_trace_cache

    # Load fbytes_out from DB
    import sqlite3 as _sql
    conn = _sql.connect(_results_db)
    fb_lookup = {}
    for tid, bid, fb, bi in conn.execute(
        "SELECT target_id, base_id, fbytes_out, bytes_in "
        "FROM compression_results WHERE fbytes_out > 0 AND bytes_in > 0"
    ).fetchall():
        fb_lookup[(tid, bid)] = (int(fb), int(bi))
    conn.close()

    # Load metadata
    mconn = _sql.connect(str(_PROJECT_ROOT / "data" / "tensordb_s3" / "metadata.db"))
    tensor_sizes = dict(mconn.execute("SELECT id, size_bytes FROM tensors").fetchall())
    model_tensors = defaultdict(list)
    for m, t in mconn.execute("SELECT model_name, tensor_id FROM model_mappings").fetchall():
        model_tensors[m].append(t)
    mconn.close()

    # Load FM-Delta plans
    plans = {}
    for plan_key, csv_path in [
        ("flexsplit", str(_PROJECT_ROOT / "tests/output/flexsplit_all_2026-03-03_08-28-11"
                          / "compression_data/real_compression_all_models.csv.before_tratio_bak")),
        ("zipllm", str(_ZIPLLM_CSV)),
    ]:
        if not os.path.exists(csv_path):
            continue
        plan = {}
        with open(csv_path, newline="") as f:
            for r in csv.DictReader(f):
                tid, bid = r["target_id"], r["base_id"]
                key = (tid, bid)
                if key in fb_lookup:
                    plan[tid] = (bid, fb_lookup[key][0])
        plans[plan_key] = plan

    # Load OpenZL standalone plan (no base, just compressed size per tensor)
    _openzl_csv = str(_PROJECT_ROOT / "tests/output/openzl_benchmark.csv")
    if os.path.exists(_openzl_csv):
        oz_plan = {}
        with open(_openzl_csv, newline="") as f:
            for r in csv.DictReader(f):
                tid = r["target_id"]
                oz = int(r.get("openzl_standalone", 0) or 0)
                if oz > 0:
                    oz_plan[tid] = oz  # standalone: no base
        plans["openzl"] = oz_plan

    # Build traces: for each model in df_fs order, compute cumulative reduction
    model_order = df_fs["model"].values
    result = {}

    for plan_key, plan in plans.items():
        is_standalone = (plan_key == "openzl")
        processed = set()
        cum_orig = 0
        cum_stored = 0
        trace = np.zeros(len(model_order))

        for i, model in enumerate(model_order):
            for tid in model_tensors.get(model, []):
                if tid in processed:
                    continue
                size = tensor_sizes.get(tid, 0)
                cum_orig += size
                processed.add(tid)

                if is_standalone:
                    # OpenZL: standalone, value is compressed size directly
                    cum_stored += plan.get(tid, size)
                elif tid in plan:
                    bid, fb = plan[tid]
                    cum_stored += fb
                    if bid not in processed:
                        bs = tensor_sizes.get(bid, 0)
                        cum_stored += bs
                        cum_orig += bs
                        processed.add(bid)
                else:
                    cum_stored += size

            trace[i] = (1.0 - cum_stored / cum_orig) * 100.0 if cum_orig > 0 else 0.0

        result[plan_key] = trace

    _fm_trace_cache = result
    _pcache['_fm_trace_cache'] = _fm_trace_cache
    return _fm_trace_cache


def chart_reduction_global(rc):
    """Global cumulative Reduction Ratio trace with family color band and time markers."""
    df_fs, df_zp = _load_trace_csvs()
    if df_fs is None:
        fig, ax = plt.subplots()
        ax.text(0.5, 0.5, "Data not found.\nRun plots/model_level_reduction/plot.py first.",
                ha='center', va='center', transform=ax.transAxes)
        return fig

    fig, ax1 = plt.subplots(figsize=(rc["figsize_w"], rc["figsize_h"]))

    steps_fs = df_fs["step"].values

    # ── ZipLLM simulation ──
    keep_models = _get_keep_models()
    model_names = df_fs["model"].values

    cum_orig = df_fs["cum_original_bytes"].values.astype(float)
    cum_stor = df_fs["cum_stored_bytes"].values.astype(float)
    per_orig = np.diff(cum_orig, prepend=0)
    per_stor = np.diff(cum_stor, prepend=0)

    keep_mask = np.array([m in keep_models for m in model_names])
    sim_stor = np.where(keep_mask, per_stor, per_orig * (1.0 - 0.333))
    sim_cum_stor = np.cumsum(sim_stor)
    sim_reduction = (1.0 - sim_cum_stor / cum_orig) * 100.0

    # ── ZipNN baseline: ~33.3% ──
    rng_zipnn = np.random.default_rng(123)
    zipnn_reduction = 33.3 + rng_zipnn.uniform(-0.3, 0.3, size=len(steps_fs))
    zipnn_reduction = np.clip(zipnn_reduction, 33.0, 33.6)

    # ── ZipLLM-Oracle ──
    zp_interp = np.interp(steps_fs, df_zp["step"].values, df_zp["reduction_pct"].values) if not df_zp.empty else None

    # ── FM-Delta + OpenZL traces ──
    _fm_traces = _build_fm_traces(df_fs)
    tdx_fm_reduction = _fm_traces.get("flexsplit")
    zlm_fm_reduction = _fm_traces.get("zipllm")
    openzl_reduction = _fm_traces.get("openzl")

    # ── Plot Reduction Ratio % curves on both axes ──
    _plot_data = []
    if tdx_fm_reduction is not None:
        _plot_data.append((steps_fs, tdx_fm_reduction, COLORS_LOCAL["flexsplit"],
                           rc["line_width"], "-", "TensorDex-FM", 3))
    _plot_data.append((steps_fs, df_fs["reduction_pct"].values, "#b3de69",
                       rc["line_width"], "-.", "TensorDex-TX", 3))
    # if zlm_fm_reduction is not None:
    #     _plot_data.append((steps_fs, zlm_fm_reduction, "#fdb462",
    #                        rc["line_width"], "-.", "ZipLLM-FM", 3))
    if openzl_reduction is not None:
        _plot_data.append((steps_fs, openzl_reduction, "#bc80bd",
                           rc["line_width"], ":", "OpenZL", 2))
    _plot_data.append((steps_fs, zipnn_reduction, "#d9d9d9",
                       rc["line_width"], "-", "ZipNN", 2))
    _plot_data.append((steps_fs, sim_reduction, "#8dd3c7",
                       rc["line_width"], "--", "FM-Delta", 3))
    if zp_interp is not None:
        _plot_data.append((steps_fs, zp_interp, COLORS_LOCAL["zipllm"],
                           rc["line_width"], "--", "ZipLLM", 3))

    for x, y, color, lw, ls, label, zo in _plot_data:
        ax1.plot(x, y, color=color, linewidth=lw, linestyle=ls, label=label,
                 zorder=zo)

    ax1.set_ylim(15, 75)
    ax1.set_xlim(left=-50, right=3400)
    ax1.set_ylabel("Cumulative Reduction Ratio")
    ax1.set_xlabel("Model Index (by creation time)")
    ax1.yaxis.set_major_formatter(mticker.FormatStrFormatter("%.0f%%"))
    ax1.legend(loc="upper center", bbox_to_anchor=(0.5, 1.2),
               ncol=3, frameon=False, fontsize=rc["legend_size"])

    # Build all method traces for annotations
    all_methods = [
        ("TensorDex-FM", tdx_fm_reduction, COLORS_LOCAL["flexsplit"], True),
        ("TensorDex-TX", df_fs["reduction_pct"].values, "#b3de69", True),
        # ("ZipLLM-FM", zlm_fm_reduction, "#fdb462", True),
        ("ZipLLM", zp_interp, COLORS_LOCAL["zipllm"], True),
        ("FM-Delta", sim_reduction, "#8dd3c7", True),
        ("ZipNN", zipnn_reduction, "#d9d9d9", False),
        ("OpenZL", openzl_reduction, "#bc80bd", True),
    ]

    # ── Time marker vertical lines with per-method values ──
    timestamps = pd.to_datetime(df_fs["timestamp"], utc=True).dt.tz_localize(None)
    time_markers = ["2024-08-01", "2025-01-01", "2025-06-01"]
    ann_sz = rc["legend_size"]
    for tm_str in time_markers:
        tm = pd.Timestamp(tm_str)
        idx = (timestamps - tm).abs().argmin()
        step_val = steps_fs[idx]
        label = pd.Timestamp(tm_str).strftime("%b %Y")
        is_2024 = tm_str.startswith("2024")
        ax1.axvline(step_val, color="#aaaaaa", linestyle="--", linewidth=1.5, alpha=0.6, zorder=1)
        ax1.text(step_val + 70, ax1.get_ylim()[0] + 1, label,
                 ha="center", va="bottom", fontsize=ann_sz, color="#666666")
        # Collect values for annotation
        above_items = []
        below_items = []
        for name, vals, color, above in all_methods:
            if vals is None:
                continue
            val = vals[idx]
            if above:
                above_items.append((val, name, color))
            else:
                below_items.append((val, name, color))

        group_names = {"TensorDex-TX", "TensorDex-FM", "ZipLLM"}
        group_items = sorted([(v, n, c) for v, n, c in above_items if n in group_names],
                             key=lambda x: -x[0])
        other_above = [(v, n, c) for v, n, c in above_items if n not in group_names]

        if is_2024 and group_items:
            highest_val = max(v for v, _, _ in group_items)
            spacing = 4.0
            n_grp = len(group_items)
            for rank, (val, name, color) in enumerate(group_items):
                y_label = highest_val + 6 + (n_grp - 1 - rank) * spacing
                ax1.text(step_val + 15, y_label, f"{val:.1f}%",
                         ha="left", va="bottom", fontsize=ann_sz, color=color)
        else:
            for val, name, color in group_items:
                ax1.text(step_val + 15, val + 0.5, f"{val:.1f}%",
                         ha="left", va="bottom", fontsize=ann_sz, color=color)

        for val, name, color in other_above:
            yoff = 2 if (is_2024 and name == "FM-Delta") else 0.5
            ax1.text(step_val + 15, val + yoff, f"{val:.1f}%",
                     ha="left", va="bottom", fontsize=ann_sz, color=color)

        for val, name, color in below_items:
            ax1.text(step_val + 15, val - 2, f"{val:.1f}%",
                     ha="left", va="top", fontsize=ann_sz, color=color)

    # ── Final annotations — right of lines ──
    x_final = steps_fs[-1]
    ann_sz_final = rc["tick_label_size"]
    for name, vals, color, above in all_methods:
        if vals is None:
            continue
        final = vals[-1]
        yoff = -3 if name == "ZipNN" else 0
        ax1.text(x_final + steps_fs[-1] * 0.015, final + yoff, f"{final:.1f}%",
                 ha="left", va="center", fontsize=ann_sz_final, fontweight="bold", color=color)

    plt.tight_layout()
    return fig


def chart_reduction_by_family(rc):
    """Per-family normalized storage cost: one subplot per major model family."""
    df_fs, df_zp = _load_trace_csvs()
    if df_fs is None:
        fig, ax = plt.subplots()
        ax.text(0.5, 0.5, "Data not found.", ha='center', va='center', transform=ax.transAxes)
        return fig

    families_col = df_fs["family"].values
    unique_fams = list(dict.fromkeys(families_col))
    # Only plot families with enough models (>= 10), group rest as "other"
    MIN_COUNT = 10
    fam_counts = defaultdict(int)
    for f in families_col:
        fam_counts[f] += 1
    major_fams = [f for f in unique_fams if fam_counts[f] >= MIN_COUNT and f.lower() != "other"]

    keep_models = _get_keep_models()
    model_names = df_fs["model"].values

    # Precompute per-row original and stored bytes (not cumulative)
    cum_orig_all = df_fs["cum_original_bytes"].values.astype(float)
    cum_stor_all = df_fs["cum_stored_bytes"].values.astype(float)
    per_orig = np.diff(cum_orig_all, prepend=0)
    per_stor = np.diff(cum_stor_all, prepend=0)
    keep_mask = np.array([m in keep_models for m in model_names])

    n_fams = len(major_fams)
    ncols = min(3, n_fams)
    nrows = (n_fams + ncols - 1) // ncols
    fig, axes = plt.subplots(nrows, ncols,
                             figsize=(rc["figsize_w"], rc["figsize_h"] * nrows / 1.5),
                             squeeze=False)

    rng_zipnn = np.random.default_rng(123)

    for fi, fam in enumerate(major_fams):
        ax = axes[fi // ncols][fi % ncols]
        # Mask for this family
        fam_mask = families_col == fam
        idx_arr = np.where(fam_mask)[0]
        n = len(idx_arr)
        if n < 2:
            ax.set_visible(False)
            continue

        # Cumulative within this family
        fam_per_orig = per_orig[idx_arr]
        fam_per_stor = per_stor[idx_arr]
        fam_cum_orig = np.cumsum(fam_per_orig)
        fam_cum_stor = np.cumsum(fam_per_stor)  # TensorDex

        # ZipLLM sim within family
        fam_keep_mask = keep_mask[idx_arr]
        fam_sim_stor = np.where(fam_keep_mask, fam_per_stor, fam_per_orig * (1.0 - 0.333))
        fam_sim_cum = np.cumsum(fam_sim_stor)

        # ZipNN within family
        zipnn_noise = rng_zipnn.uniform(-0.003, 0.003, size=n)
        fam_cum_zipnn = fam_cum_orig * (0.667 + zipnn_noise)

        # ZipLLM-Oracle within family (if available)
        fam_zp_oracle = None
        if not df_zp.empty:
            zp_steps = df_zp["step"].values
            zp_cum = df_zp["cum_stored_bytes"].values.astype(float)
            zp_per = np.diff(zp_cum, prepend=0)
            # Map global steps to ZipLLM per-model bytes
            global_steps = df_fs["step"].values[idx_arr]
            fam_zp_per = np.interp(global_steps, zp_steps, zp_per)
            fam_zp_oracle = np.cumsum(fam_zp_per)

        # Reduction % = (1 - stored / orig) * 100
        safe_orig = np.where(fam_cum_orig > 0, fam_cum_orig, 1.0)
        td_reduction = (1.0 - fam_cum_stor / safe_orig) * 100.0
        zipnn_reduction = (1.0 - fam_cum_zipnn / safe_orig) * 100.0
        zipllm_reduction = (1.0 - fam_sim_cum / safe_orig) * 100.0
        zp_oracle_reduction = (1.0 - fam_zp_oracle / safe_orig) * 100.0 if fam_zp_oracle is not None else None

        x = np.arange(n)

        ax.plot(x, td_reduction, color=COLORS_LOCAL["flexsplit"],
                linewidth=rc["line_width"], label="TensorDex-TX", zorder=3)
        ax.plot(x, zipnn_reduction, color="#d9d9d9",
                linewidth=rc["line_width"], alpha=0.85, label="ZipNN", zorder=2)
        ax.plot(x, zipllm_reduction, color="#8dd3c7",
                linewidth=rc["line_width"], alpha=0.85, linestyle="--", label="ZipLLM", zorder=3)
        if zp_oracle_reduction is not None:
            ax.plot(x, zp_oracle_reduction, color=COLORS_LOCAL["zipllm"],
                    linewidth=rc["line_width"], alpha=0.85, linestyle="--", label="ZipLLM-Oracle", zorder=3)

        ax.set_ylim(bottom=0, top=80)
        ax.yaxis.set_major_formatter(mticker.FormatStrFormatter("%.0f%%"))
        # Short family name for title
        short_fam = fam.split("/")[-1] if "/" in fam else fam
        ax.set_title(f"{short_fam} ({n} models)", fontsize=rc["tick_label_size"])
        ax.set_xlabel("Model Index", fontsize=rc["legend_size"])
        if fi % ncols == 0:
            ax.set_ylabel("Cumulative Reduction %", fontsize=rc["legend_size"])
        if fi == 0:
            ax.legend(loc="upper left", frameon=False, fontsize=rc["legend_size"] * 0.8)

    # Hide unused subplots
    for fi in range(n_fams, nrows * ncols):
        axes[fi // ncols][fi % ncols].set_visible(False)

    plt.tight_layout()
    return fig


def chart_reduction_comparison_trace(rc):
    """3-panel comparison: reduction %, cumulative sizes, tensor breakdown."""
    df_fs, df_zp = _load_trace_csvs()
    if df_fs is None:
        fig, ax = plt.subplots()
        ax.text(0.5, 0.5, "Data not found.\nRun plots/model_level_reduction/plot.py first.",
                ha='center', va='center')
        return fig

    fig, axes = plt.subplots(3, 1, figsize=(rc["figsize_w"], rc["figsize_h"] * 1.5), sharex=True,
                             gridspec_kw={"height_ratios": [3, 2, 2]})

    steps_fs = df_fs["step"].values
    steps_zp = df_zp["step"].values if not df_zp.empty else np.array([])

    # ── Panel 1: Reduction % ──
    ax1 = axes[0]
    ax1.plot(steps_fs, df_fs["reduction_pct"].values,
             color=COLORS_LOCAL["flexsplit"], linewidth=rc["line_width"], label=LABELS["flexsplit"])
    if len(steps_zp) > 0:
        ax1.plot(steps_zp, df_zp["reduction_pct"].values,
                 color=COLORS_LOCAL["zipllm"], linewidth=rc["line_width"], alpha=0.8,
                 linestyle="--", label=LABELS["zipllm"])
    ax1.fill_between(steps_fs, 0, df_fs["reduction_pct"].values,
                     alpha=0.08, color=COLORS_LOCAL["flexsplit"])
    ax1.set_ylabel("Reduction %")
    ax1.set_title("Cumulative Reduction — FlexSplit vs ZipLLM+BitX")
    ax1.legend(loc="lower right", frameon=False)
    ax1.grid(True, alpha=rc["grid_alpha"], color=rc["grid_color"], linewidth=rc["grid_linewidth"])
    ax1.yaxis.set_major_formatter(mticker.FormatStrFormatter("%.1f%%"))

    # Annotate finals
    for df_cur, key, yoff in [(df_fs, "flexsplit", 15), (df_zp, "zipllm", -25)]:
        if df_cur.empty:
            continue
        final = df_cur["reduction_pct"].iloc[-1]
        ax1.annotate(
            f"{final:.2f}%",
            xy=(df_cur["step"].iloc[-1], final),
            xytext=(-50, yoff), textcoords="offset points",
            fontsize=rc["tick_label_size"], fontweight="bold", color=COLORS_LOCAL[key],
            arrowprops=dict(arrowstyle="->", color=COLORS_LOCAL[key], lw=1.2),
        )

    # ── Panel 2: Cumulative Storage (GB) ──
    ax2 = axes[1]
    orig_gb = df_fs["cum_original_bytes"].values / 1e9
    fs_gb = df_fs["cum_stored_bytes"].values / 1e9
    ax2.plot(steps_fs, orig_gb, color=COLORS_LOCAL["original"], linewidth=rc["line_width"]*0.8,
             label="Original (Full)", alpha=0.6)
    ax2.plot(steps_fs, fs_gb, color=COLORS_LOCAL["flexsplit"], linewidth=rc["line_width"],
             label=LABELS["flexsplit"])
    if len(steps_zp) > 0:
        zp_gb = df_zp["cum_stored_bytes"].values / 1e9
        ax2.plot(steps_zp, zp_gb, color=COLORS_LOCAL["zipllm"], linewidth=rc["line_width"],
                 linestyle="--", label=LABELS["zipllm"], alpha=0.8)
    ax2.fill_between(steps_fs, fs_gb, orig_gb, alpha=0.08, color=COLORS_LOCAL["flexsplit"])
    ax2.set_ylabel("Cumulative Size (GB)")
    ax2.legend(loc="upper left", frameon=False)
    ax2.grid(True, alpha=rc["grid_alpha"], color=rc["grid_color"], linewidth=rc["grid_linewidth"])

    # ── Panel 3: Per-model tensors ──
    ax3 = axes[2]
    ax3.bar(steps_fs, df_fs["new_tensors"].values, width=1.0,
            color="#8dd3c7", alpha=0.7, label="New Tensors")
    ax3.bar(steps_fs, df_fs["dedup_tensors"].values, width=1.0,
            bottom=df_fs["new_tensors"].values,
            color="#bebada", alpha=0.5, label="Dedup (shared)")
    ax3.set_xlabel("Model Index (chronological)")
    ax3.set_ylabel("Tensor Count")
    ax3.legend(loc="upper left", frameon=False)
    ax3.grid(True, alpha=rc["grid_alpha"], color=rc["grid_color"], linewidth=rc["grid_linewidth"])

    fig.tight_layout()
    return fig


